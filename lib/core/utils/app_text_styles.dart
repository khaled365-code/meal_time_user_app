double getResponsiveFontSize({required BuildContext context,required double fontSize})
{
  double scaleFactor=getScaleFactor(context: context);
  double responsiveFontSize=scaleFactor*fontSize;
  double minFontSize=fontSize*.8;
  double maxFontSize=fontSize*1.2;

  return responsiveFontSize.clamp(minFontSize, maxFontSize);
}

double getScaleFactor({required BuildContext context})
{
  double width=MediaQuery.of(context).size.width;
  if(width<SizeConfig.tabletWidth)
  {
    return width/400;
  }
  else if(width<SizeConfig.desktopWidth)
  {
    return width/700;
  }
  else
  {
      return width/1000;
  }
}